  function getPageKey() {
    const pageFromAttr = document.body && document.body.getAttribute("data-i18n-page");
    if (pageFromAttr) return pageFromAttr;
    const path = window.location.pathname;
    const segments = path.split("/").filter(Boolean);
    const file = segments.length ? segments[segments.length - 1] : "index.html";
    if (file.endsWith(".html")) return file.replace(".html", "") || "index";
    return file || "index";
  }

  function toSupportedLanguage(lang) {
    if (!lang) return null;
    const normalized = String(lang).toLowerCase().slice(0, 2);
    return SUPPORTED_LANGUAGES.includes(normalized) ? normalized : null;
  }

  function normalizeLanguage(lang) {
    return toSupportedLanguage(lang) || DEFAULT_LANGUAGE;
  }

  function getSavedLanguageStrict() {
    return toSupportedLanguage(localStorage.getItem(STORAGE_KEY));
  }

  function getSavedLanguage() {
    return getSavedLanguageStrict() || DEFAULT_LANGUAGE;
  }

  function getBrowserLanguage() {
    const candidates = Array.isArray(navigator.languages) && navigator.languages.length
      ? navigator.languages
      : [navigator.language];
    for (const candidate of candidates) {
      const supported = toSupportedLanguage(candidate);
      if (supported) return supported;
    }
    return null;
  }

  function resolveInitialLanguage() {
    return getSavedLanguageStrict() || getBrowserLanguage() || DEFAULT_LANGUAGE;
  }

  function getTranslation(lang, key) {
    const pageKey = getPageKey();
    const page = TRANSLATIONS.pages[pageKey];
    if (page && page[lang] && key in page[lang]) return page[lang][key];
    if (page && page.en && key in page.en) return page.en[key];
    if (TRANSLATIONS.common[lang] && key in TRANSLATIONS.common[lang]) return TRANSLATIONS.common[lang][key];
    if (TRANSLATIONS.common.en && key in TRANSLATIONS.common.en) return TRANSLATIONS.common.en[key];
    return "";
  }

  function applyTranslations(lang) {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      const value = getTranslation(lang, key);
      if (key === "quote_attr") {
        if (!value) {
          el.innerHTML = "";
          el.style.display = "none";
          return;
        }
        el.style.display = "";
      }
      if (!value) return;
      el.innerHTML = value;
    });

    document.querySelectorAll("[data-i18n-attr]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      const value = getTranslation(lang, key);
      if (!value) return;
      const attrs = el.getAttribute("data-i18n-attr").split(",").map((a) => a.trim()).filter(Boolean);
      attrs.forEach((attr) => el.setAttribute(attr, value));
    });
  }

  function updateLanguageSwitcher(lang) {
    document.querySelectorAll(".gk-lang-btn").forEach((btn) => {
      const isActive = btn.getAttribute("data-lang") === lang;
      btn.classList.toggle("active", isActive);
      btn.setAttribute("aria-pressed", isActive ? "true" : "false");
    });
  }

  function updateDocumentMetadata(lang) {
    const title = getTranslation(lang, "meta_title");
    const description = getTranslation(lang, "meta_description");
    if (title) document.title = title;
    const meta = document.querySelector('meta[name="description"]');
    if (meta && description) meta.setAttribute("content", description);
    document.documentElement.setAttribute("lang", lang);
  }

  function setLanguage(lang) {
    const normalized = normalizeLanguage(lang);
    localStorage.setItem(STORAGE_KEY, normalized);
    applyTranslations(normalized);
    updateLanguageSwitcher(normalized);
    updateDocumentMetadata(normalized);
  }

  function initializeI18n() {
    document.querySelectorAll(".gk-lang-btn").forEach((btn) => {
      btn.addEventListener("click", () => setLanguage(btn.getAttribute("data-lang")));
    });
    setLanguage(resolveInitialLanguage());
  }

  window.GK_i18n = {
    getSavedLanguage,
    normalizeLanguage,
    setLanguage,
    applyTranslations,
    updateLanguageSwitcher,
    updateDocumentMetadata,
    initializeI18n
  };

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", initializeI18n);
  } else {
    initializeI18n();
  }
