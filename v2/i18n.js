/* ── ENGINE ────────────────────────────────────────────────── */

let currentLang = DEFAULT_LANG;

function detectBrowserLang() {
  const saved = localStorage.getItem('gk_lang');
  if (saved && SUPPORTED.includes(saved)) return saved;
  const browser = (navigator.language || navigator.userLanguage || '').toLowerCase().slice(0, 2);
  return SUPPORTED.includes(browser) ? browser : DEFAULT_LANG;
}

function t(key) {
  const lang = translations[currentLang] || translations[DEFAULT_LANG];
  return lang[key] ?? translations[DEFAULT_LANG][key] ?? null;
}

function applyTranslations(lang) {
  currentLang = lang;
  localStorage.setItem('gk_lang', lang);
  document.documentElement.lang = lang;

  // text/html content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val !== null) el.innerHTML = val;
  });

  // placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    const val = t(key);
    if (val !== null) el.placeholder = val;
  });

  // lang buttons active state
  document.querySelectorAll('.lang-btn, .mobile-lang button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

function switchLang(lang) {
  if (!SUPPORTED.includes(lang)) return;
  applyTranslations(lang);
}

// Init on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  const lang = detectBrowserLang();
  applyTranslations(lang);

  // Wire up all lang buttons (desktop + mobile)
  document.querySelectorAll('.lang-btn, .mobile-lang button').forEach(btn => {
    btn.addEventListener('click', () => switchLang(btn.dataset.lang));
  });
});
